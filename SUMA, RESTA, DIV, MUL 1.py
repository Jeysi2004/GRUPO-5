n1=int(input("Ingrese el primer número: "))
n2=int(input("Ingrese el segundo número: "))
def suma():
    return n1+n2
print("La suma es: ",suma())
def resta():
    return n1-n2
print ("La resta es: ",resta())

def mul():
    return n1*n2
print ("La multiplicación es: ",mul())

def div():
    return n1/n2
print ("La división es : ",div())
